          'name' := Name,
          'image' := Image,
          'gameplay' := Gameplay,
          'link' := Link,
          'x_url' := XUrl,
          'rating' := Rating,
          'description' := Description,
          'short_description' := ShortDescription,
          'release_date' := ReleaseDate,
          'developer' := Developer,
          'playtime' := Playtime,
          'platforms' := Platforms,
          'tags' := Tags,
          'genres' := Genres,
          'genre' := Genre,
          'themes' := Themes,
          'adult_only' := AdultOnly,
          'play_modes' := PlayModes,
          'screenshots' := Screenshots,
          'videos' := Videos,
          'offers' := Offers,
          'official_stores' := OfficialStores,
          'micro_trailer' := MicroTrailer
        }) ->
    #{ 'id' => Id,
       'name' => Name,
       'image' => Image,
       'gameplay' => Gameplay,
       'link' => Link,
       'x_url' => XUrl,
       'rating' => Rating,
       'description' => Description,
       'short_description' => ShortDescription,
       'release_date' => ReleaseDate,
       'developer' => Developer,
       'playtime' => Playtime,
       'platforms' => Platforms,
       'tags' => Tags,
       'genres' => Genres,
       'genre' => Genre,
       'themes' => Themes,
       'adult_only' => AdultOnly,
       'play_modes' => PlayModes,
       'screenshots' => Screenshots,
       'videos' => Videos,
       'offers' => Offers,
       'official_stores' => OfficialStores,
